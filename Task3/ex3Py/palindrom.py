def is_palindrome(num):
    """Проверяет, является ли число трехзначным палиндромом"""
    num = abs(num)  # Работаем с модулем числа (убираем знак)
    if num < 100 or num > 999:  # Проверка что число трехзначное
        return False
    return (num // 100) == (num % 10)  # Сравниваем первую и последнюю цифру

def main():
    print("Введите числа через пробел: ", end='')  # Запрос ввода без перевода строки
    input_str = input().strip()  # Получаем и очищаем ввод от пробелов по краям
    
    print("Результат: ", end='')  # Начало вывода результата
    first = True  # Флаг первого вывода для правильной расстановки запятых
    i = 0  # Индекс для прохода по строке ввода
    
    # Парсим строку ввода посимвольно
    while i < len(input_str):
        # Обрабатываем цифры и отрицательные числа
        if input_str[i].isdigit() or (input_str[i] == '-' and i+1 < len(input_str) and input_str[i+1].isdigit()):
            num_str = ''  # Собираем строку числа
            
            # Обрабатываем знак минус
            if input_str[i] == '-':
                num_str += input_str[i]
                i += 1
            
            # Собираем все цифры числа подряд
            while i < len(input_str) and input_str[i].isdigit():
                num_str += input_str[i]
                i += 1
            
            num = int(num_str)  # Преобразуем в число
            
            # Проверяем трехзначность
            if abs(num) >= 100 and abs(num) <= 999:
                if not first:  # Добавляем запятую перед элементами, кроме первого
                    print(", ", end='')
                # Выводим число и результат проверки (YES/NO)
                print(f"{num} - {'YES' if is_palindrome(num) else 'NO'}", end='')
                first = False  # После первого вывода меняем флаг
        else:
            i += 1  # Пропускаем нечисловые символы
    
    # Если не было найдено трехзначных чисел
    if first:
        print("нет трехзначных чисел", end='')
    print()  # Перевод строки в конце

if __name__ == "__main__":
    main()  # Запуск программы